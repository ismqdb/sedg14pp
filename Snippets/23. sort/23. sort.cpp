void selectionSort(int a[], int n){
    int min;
    int temp;

    for(int i = 0; i < n; i++){
        min = i;

        for(int k = i; k < n; k++)
            if(a[k] < a[min])
                min = k;

        temp = a[i];
        a[i] = a[min];
        a[min] = temp;
    }
}

/* ******************************************************************************** */

void insertionSort(int a[], int n){
    for(int i = 1; i <= n; i++){
        int k = i;
        int temp = a[k];

        while(temp < a[k-1]){
            a[k] = a[k-1];
            k--;
        }
        a[k] = temp;
    }
}

/* ******************************************************************************** */

void bubbleSort(int a[], int n){
    int t;

    for(int i = n; i >= 1; i--)
        for(int j = 0; j <= i; j++)
            if(a[j-1] > a[j]){
                t = a[j-1];
                a[j-1] = a[j];
                a[j] = t;
            }
}

