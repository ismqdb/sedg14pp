void selectionSort(int a[], int n){
    int min;
    int t;

    for(int i = 1; i < n; i++){
        min = i;
        for(int j = i+1; j <= n; j++)
            if(a[j] < a[min])
                min = j;

        t = a[min];
        a[min] = a[i];
        a[i] = t;
    }
}

/* ******************************************************************************** */

void insertionSort(int a[], int n){
    int v;
    int j;

    for(int i = 2; i <= n; i++){
        v = a[i];
        j = i;

        while(a[j-1] > v){
            a[j] = a[j-1];
            j--;
        }
        a[j] = v;
    }
}

/* ******************************************************************************** */

void bubbleSort(int a[], int n){
    int t;

    for(int i = n; i >= 1; i--)
        for(int j = 0; j <= i; j++)
            if(a[j-1] > a[j]){
                t = a[j-1];
                a[j-1] = a[j];
                a[j] = t;
            }
}

