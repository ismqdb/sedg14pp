#include <iostream>
#include <vector>

/* ******************************************************************************** */

size_t gcd(size_t u, size_t v){
    size_t t;

    while(u > 0){
        if(u < v)
            std::swap(u, v);
        u = u-v;
    }
    return v;
}

/* ******************************************************************************** */

int main(){
    std::vector<std::vector<int>> array;

    for(int i = 1; i < array.size(); i++)
        for(int j = 1; j < array[i].size(); j++)
            if(gcd(i, j) == 1)
                array[i][j] = 1;
            else
                array[i][j] = 0;

    putchar(10); 
}

