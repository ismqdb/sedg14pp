#include "./12. stackA.hpp"

/* ******************************************************************************** */

int stackA::isEmpty(){
    return !p;
}

/* ******************************************************************************** */

void stackA::push(int v){
    this->stack.push_back(v);
    p++;
}

void stackA::pop(){
    if(p > 0){
        p--;
        this->stack.pop_back();
    }
}

int stackA::top(){
    if(!isEmpty())
        return stack[p-1];
    else
        throw "Top(): empty stack.";
}

/* ******************************************************************************** */

stackA::stackA(){
    p = 0;
}

stackA::~stackA(){
    p = 0;
}

