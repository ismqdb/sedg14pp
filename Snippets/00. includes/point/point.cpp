point::point(){}

/* ******************************************************************************** */

point::point(i32 x, i32 y){
    assert(x >= 0);
    assert(y >= 0);

    this->x = x;
    this->y = y;
}

/* ******************************************************************************** */

bool isSamePoint(point *pt1, point *pt2){
    return pt1->x == pt2->x && pt1->y == pt2->y;
}

/* ******************************************************************************** */

point* middlePoint(point *pt1, point *pt2){
    point *pt = new point{};
    pt->x = (pt1->x+pt2->x)/2;
    pt->y = (pt1->y+pt2->y)/2;
    return pt;
}

