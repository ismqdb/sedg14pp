none lineSegment(point *pt1, point *pt2){
    if(!isSamePoint(pt1, pt2)){
        point *m = middlePoint(pt1, pt2);
        drawPoint(m);
        lineSegment(pt1, m);
        lineSegment(m, pt2);
    }
}

