int calculate2dArray(){
    std::vector<std::vector<int>> array;

    for(int i = 1; i < array.size(); i++)
        for(int j = 1; j < array[i].size(); j++)
            if(gcd(i, j) == 1)
                array[i][j] = 1;
            else
                array[i][j] = 0;

    putchar(10); 

    return 1;
}

